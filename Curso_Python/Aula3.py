"""
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Luiz Gustavo')
print(1, 'Luiz "Gustavo"')


# Aspas duplas
print("Luiz Gustavo")
print(2, "Luiz 'Gustavo'")


# Escape
print("Luiz \"Gustavo\"")

# r
print(r"Luiz \"Gustavo\"")
