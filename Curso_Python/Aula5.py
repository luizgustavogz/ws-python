# Tipo de dado bool (booleano)
# Ao questionar algo em um programa, só existem duas respostas possíveis:
# True (verdadeiro) ou False (falso)
# Existem vários operadores para "questionar"
# Dentre eles o ==, que é um operador lógico que questiona se o valor é igual a outro valor
print(10 == 10)
print(10 == 11)
print(type(True), type(False), type(10 == 10), type(10 == 11))